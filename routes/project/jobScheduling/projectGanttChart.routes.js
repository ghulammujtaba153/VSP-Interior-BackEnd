import express from "express";
import { createGanttChart, getGanttChartByJob, updateGanttChart, deleteGanttChart } from "../../../controller/project/jobScheduling/projectGanttChart.controller.js";


const projectGanttChartRouter = express.Router();



projectGanttChartRouter.post("/create", createGanttChart);
