import express from "express";
import { createProject, deleteProject, getAllProjects, getProjectById, updateProject } from "../../controller/project/project.controller.js";

const projectRouter = express.Router();

projectRouter.post("/create", createProject);

projectRouter.get("/get", getAllProjects);

projectRouter.get("/get/:id", getProjectById);

projectRouter.put("/update/:id", updateProject);


projectRouter.delete("/delete/:id", deleteProject);





export default projectRouter;